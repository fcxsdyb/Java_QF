package Stage_01.Inheritance_Practice;

public class Citizen {

	int id;
	String name;
	String birth;

	public int getId() {
		return this.id;
	}

	public String getName() {
		return this.name;
	}

	public String getBirth() {
		return this.birth;
	}

	public void setId(int id) {
		this.id = id;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setBirth(String birth) {
		this.birth = birth;
	}

}
